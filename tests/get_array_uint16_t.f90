PROGRAM GET_ARRAY_UINT16_T

  USE XDR
  USE ASSERT
  USE HELPERS
  USE, INTRINSIC :: ISO_C_BINDING, ONLY : C_INT32_T, C_SIZE_T
  IMPLICIT NONE

  TYPE(XDR_T) :: XDRS
  CHARACTER(LEN = 256) :: FILENAME
  INTEGER(KIND = C_INT32_T), DIMENSION(:), ALLOCATABLE :: VALUES
  LOGICAL :: CODE

  CALL FILE_PATH('array_uint16.xdr', FILENAME)
  CALL XDRS%READ_FILE(FILENAME)

  CODE = XDRS%GET_UNSIGNED_ARRAY(VALUES, 3_C_SIZE_T)
  CALL ASSERT_TRUE(CODE)
  CALL ASSERT_EQUALS(    0, INT(VALUES(1)))
  CALL ASSERT_EQUALS(32768, INT(VALUES(2)))
  CALL ASSERT_EQUALS(65535, INT(VALUES(3)))
  DEALLOCATE(VALUES)

  CALL XDRS%DESTROY()

END PROGRAM
